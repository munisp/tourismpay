"""
Suspicious Activity Report (SAR) Auto-Filing System

Generates and files SARs with regulatory authorities when:
  - ML risk score exceeds threshold (>0.8)
  - Transaction patterns match known typologies
  - Manual analyst escalation triggers filing
  - Structuring (smurfing) detected

Supports multiple jurisdictions:
  - Nigeria (NFIU — Nigerian Financial Intelligence Unit)
  - Kenya (FRC — Financial Reporting Centre)
  - Ghana (FIC — Financial Intelligence Centre)
  - South Africa (FIC — Financial Intelligence Centre)

FATF Travel Rule implementation:
  - Originator/beneficiary data sharing for transfers >$1000
  - IVMS101 message format (InterVASP Messaging Standard)
  - Counterparty VASP discovery and data exchange
"""
